package com.nikosgig.weatherforecastmvvm.internal

import kotlinx.coroutines.*

//todo digest this
fun <T> lazyDeferred(block: suspend CoroutineScope.() -> T): Lazy<Deferred<T>> {
    return lazy {
        GlobalScope.async(start = CoroutineStart.LAZY) {
            block.invoke(this)
        }
    }
}